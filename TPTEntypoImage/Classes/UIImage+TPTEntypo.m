+ (UIImage *)imageWithEntypoNamed:(NSString *)iconName
                           ofSize:(CGFloat)size
{
    return [self imageWithEntypoNamed:iconName
                    colored:[UIColor blackColor]
                     ofSize:size];
}

+ (UIImage *)imageWithEntypoNamed:(NSString *)iconName
                          colored:(UIColor *)color
                           ofSize:(CGFloat)size
{
    UIFont *font = [UIFont fontWithName:@"Entypo" size:size];
    
    NSDictionary *attributes = @{NSFontAttributeName : font,
                                 NSForegroundColorAttributeName : color};
    
    CGSize pixelSize = [iconName sizeWithAttributes:attributes];
    
    UIImage *image = nil;
    UIGraphicsBeginImageContextWithOptions(pixelSize, NO, 0);
    
    if (shadow) {
        CGContextSetShadowWithColor(UIGraphicsGetCurrentContext(),
                                    CGSizeMake(0.5, 0.5), 0.5,
                                    [UIColor blackColor].CGColor);
    }
    
    [iconName drawInRect:CGRectMake(0, 0, pixelSize.width, pixelSize.height) withAttributes:attributes];
    
    image = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    
    return image;
}

